         <h3 style="font-size: 23px;background-color: #1C378A;color: white;font-weight:500;padding-left: 5px">Laboratories</h3>
<br> 


          
         <h6>  1. Electrical Machines-I Laboratory</h6>             
           
<p align="justify"             >This laboratory provides the students to verify the theory in practical. To get familiar with DC machines, and give them experimental skills. This laboratory is to understand the basic principles of operation of rotating electric machines, their classification, basic efficiency &amp; performance characteristics and to calculate the various parameters.</p>

           <h6> 2.Electrical Machines- II Laboratory</h6>             
           
<p align="justify"             >This laboratory aim is providing a platform for students to perform experiments which explain the working principle and characteristics for various types of Transformers, Alternators and AC Motors. In this laboratory, students had the possibilities to test the efficiency of various tests and to determine performance characteristics which are useful in determining motors or generators to their applications. In this laboratory students have the chances to learn about the construction, working principle, performance characteristics and testing of AC machines, it makes industry ready engineers serve the society.</p>

            <h6>3.Control Systems Laboratory</h6>             
           
<p align="justify"             >Control System applications are assuming an increasingly important role in the developing world, making it crucial for the students of Electrical and Electronics Engineering (EEE) to be exposed to contemporary control system equipment in a realistic manner, in order to connect theoretical material taught in lecture courses with the realities of physical hardware and simulation through 'higher-level' technical computing language. Paramount to all laboratory equipment is the principle that students of Electrical and Electronics Engineering (EEE) should work with hardware and be allowed to take a hands-on approach. To achieve the goal, Control Systems laboratory has been set up to provide the realistic environment. This laboratory makes the students of Electrical and Electronics Engineering (EEE) understand the practical aspect of control system. How a control system is used for controlling errors. How the error compensation is done according to the system requirements. PID controller is used in many industries for the removal of error so that the fluctuations in the input (noise or any unwanted parameter) should not affect the system parameters. The lab has kits like Speed torque characteristics AC servo motor, Speed torque characteristics DC servo motor, Synchro transmitter and receiver trainer, PID controller kit, and compensation network study kit etc.</p>
<h6>4. Electrical Measurements Laboratory</h6>

<p align="justify"             >The laboratory is involved in all the areas of study, related to measurement and calibration. It is equipped with all measuring instruments, bridge circuits and induction coil etc. Here students learn to calibrate the meters, measurements of unknown parameter value and important concepts related to measuring.</p>
<h6>5. Electrical Simulation Laboratory</h6>             
           
<p align="justify"             >This laboratory enables to simulate a given electrical circuits in any environment to analyze its dynamic characteristics and to figure out its stability considerations. In this lab student able to analyze dynamic performance of various loads those are connected to any electric circuits. Students know the analysis and studies using the software and able to design circuits as per the specifications and also able to find the stability of system.</p>
<h6>6.Electrical Circuit Analysis</h6>             
           
<p align="justify"             >This laboratory provides the provision to practical verification of the circuit theorems which are learned in class room. In this laboratory, student is able to determine response and parameters of the electrical circuit. Student can be able to calculate the resistance at which maximum power is transferred and the frequency at which the phenomenon of resonance occurred in given electrical circuit.</p>
         
 <h6>7. Power Electronics Laboratory</h6>
<p align="justify"             >The power electronics lab enhances the EEE students by providing them the better understanding of the concepts and working of advanced power semiconductor devices and power electronics circuits. This laboratory enables the student to know the different types of firing and commutation circuits.</p>

<h6>8.Power Systems &amp; Simulation Laboratory</h6>
<p align="justify"             >Power system Laboratory comprises of protection, simulation, high voltage and machine related experiments. Facilities are available for over-current, under-voltage, directional, differential and distance relays including different numerical relays. Varieties of Power system Simulation packages like Load flow, PSCAD and MiPower are available</p>

<h6>9. Power converter and Drives Laboratory</h6>

<p align="justify"             >The Power Converter Laboratory mission is to investigate new and advanced power converter topologies and control strategies that help to push the power conversion efficiency, power density and reliability to new heights. The laboratory is equipped with latest hardware, real-time data measurement and control platforms in order to investigate efficiency, reliability and control under different mission profiles.</p>
                       
                          
                          
							
                          
                          
                         



