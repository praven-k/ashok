                        <b>  <font color="#FF0066">Vision: </font>	</b>
                          	<br>
							To evolve as a center of high repute in the Computer Science and Engineering and produce computer software professionals through creative and innovative teaching learning process.


                          </p>
                          <br>
                         
                          	<b>  <font color="#FF0066">Mission: </font>	</b>
                          	<br>
							<ul>
							<li style>Impart quality education through innovative teaching learning methodologies to meet the challenges in the professional career.
							<li>Pursue research and consultancy activities using state-of-the-art facilities and Industry interaction.
							<li>Pursue life-long learning to demonstrate solutions for societal problems as an entrepreneur.
							</ul>
							
                          
                          <p align="justify">
                          	
                          	   Department of CSE has well-qualified and experienced faculty who are specialists in the areas of Databases, Data Mining, Computer Architecture, Operating Systems, Image Processing, Wireless Networks, Artificial Neural Networks, Information Security and Programming Languages. There are around 80 dedicated faculty members with good amount of experience in these different areas. The faculty members are actively involved in research activities in the field of their specialization. They have published number of papers in journals and Conferences of National and International repute. The Department attributes its success to the creative and innovative outlook of its students also. The Department encourages students to participate in numerous symposiums and to present papers in them. Students are also made to undergo in-plant training programs, where they hone their technical skill in the realm of computers.
                          </p>
                          
                         <p align="justify">
                          	The department effectively prepares students to pursue leadership, technical, and management positions in a variety of industries. Students have obtained successful top-notch placements at leading companies like IBM, Infosys, Wipro, Cognizant, TCS, Satyam, and other leading companies.
                          </p>
                          
