                              <p>  <font color="#000"><b>Program Educational Objectives(PEOs)</b></font></p>
                             <b> Graduates of Information Technology Program should be able to</p>
                <p><b>PEO1:</b> To have a successful interdisciplinary career either in industry or entrepreneur both within the country or abroad</p>
                <p><b>PEO2:</b> To apply research principles and skills to identify, conceptualize, design, develop and implement the appropriate solutions for real-life problems in a sensible manner
            </p>
            <p><b>PEO3:</b>To exhibit administrative, communication skills, team spirit, leadership capabilities with professional ethics and human values.</p>


            <p>  <font color="#000"><b>Programme Specific Outcomes (PSOs)</b></font></p>

            <p align="justify"><b>PSO1:</b><b>Professional Skills:</b> Develop innovative algorithms in the field of system software, multimedia, web design, big data analytics, and networking.
            </p>

            <p align="justify"><b>PSO2:</b>:<b>Problem-Solving Skills:</b> Design & Implement software based solutions for the IT industryDesign & Implement software based solutions for the IT industry.</p>

              
                              <font color="#000"><b>Programme Outcomes (POs)</b></font></p></font>
                              <p align="justify"><b>PO1: Engineering knowledge:</b> Apply the knowledge of mathematics, science, engineering
                                 fundamentals, and an engineering specialization to the solution of complex engineering problems.
                              </p>
                              <p align="justify"><b>PO2: Problem analysis: </b>Identify, formulate, review research literature, and analyze complex engineering problems reaching substantiated conclusions using first principles of mathematics, natural sciences, and engineering sciences. </p>
                              <p align="justify"><b>PO3:Design/development of solutions:</b> Design solutions for complex engineering problems and design system components or processes that meet the specified needs with appropriate consideration for the public health and safety, and the cultural, societal, and environmental considerations.</p>
                              <p align="justify"><b>PO4: Conduct investigations of complex problems:</b> Use research-based knowledge and research methods including design of experiments, analysis and interpretation of data, and synthesis of the information to provide valid conclusions.</p>
                              <p align="justify"><b>PO5: Modern tool usage: </b>Create, select, and apply appropriate techniques, resources, and modern engineering and IT tools including prediction and modeling to complex engineering activities with an understanding of the limitations.</p>
                              <p align="justify"><b>PO6: The engineer and society: </b>Apply reasoning informed by the contextual knowledge to assess societal, health, safety, legal and cultural issues and the consequent responsibilities relevant to the professional engineering practice. </p>
                              <p align="justify"><b>PO7: Environment and sustainability: </b>Understand the impact of the professional engineering solutions in societal and environmental contexts, and demonstrate the knowledge of, and need for sustainable development.</p>
                              <p align="justify"><b>PO8: Ethics: </b>Apply ethical principles and commit to professional ethics and responsibilities and norms of the engineering practice.</p>
                              <p align="justify"><b>PO9: Individual and team work:</b> Function effectively as an individual, and as a member or leader in diverse teams, and in multidisciplinary settings.</p>
                              <p align="justify"><b>PO10: Communication: </b>Communicate effectively on complex engineering activities with the engineering community and with society at large, such as, being able to comprehend and write effective reports and design documentation, make effective presentations, and give and receive clear instructions.</p>
                              <p align="justify"><b>PO11: Project management and finance:</b> Demonstrate knowledge and understanding of the leader in a team, to manage projects and in multidisciplinary environments.</p>
                              <p align="justify"><b>PO12: Life-long learning: </b>Recognize the need for, and have the preparation and ability to engage in independent and life-long learning in the broadest context of technological change.</p>
